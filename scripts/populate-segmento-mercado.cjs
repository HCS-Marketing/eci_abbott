// Derive Mercado from Segmento
function getMercado(segmento) {
  if (SEGMENTO_TO_MERCADO[segmento]) return SEGMENTO_TO_MERCADO[segmento]
  // For unique brand-segmentos (like BIBS, Nuby, etc.), classify as Otras
  return 'Otras'
}

